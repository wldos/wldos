package com.wldos.system.core.controller;

import java.util.Map;

import com.wldos.support.controller.RepoController;
import com.wldos.support.controller.web.PageableResult;
import com.wldos.support.util.PageQuery;
import com.wldos.system.core.entity.WoOptions;
import com.wldos.system.core.service.OptionsService;

import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;

/**
 * 系统配置相关controller。
 *
 * @author 树悉猿
 * @date 2021/5/2
 * @version 1.0
 */
@RestController
@RequestMapping("admin/sys/options")
public class OptionsAdminController extends RepoController<OptionsService, WoOptions> {
	@GetMapping("")
	public String readOptions() {
		Map<String, String> options = System.getenv();
		return this.resJson.ok(options);
	}
}
